    def test_read_with_manual_flush_many_writes(self):

        num = 1000

        for i in range(num):
            f = BigIndexObject(counter=i)
            f.set_random_properties()
            f.put()

            # This gets us to Milestone A, which means retrieving this entity
            # will be consistent, but does not get us to Milestone B, which
