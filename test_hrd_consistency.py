    def test_read_with_manual_flush_many_threads(self):
        global finished_count
        finished_count_lock = threading.Lock()
        finished_count = 0

        all_finished_event = threading.Event()

        num = 10

        def run(i):
            f = BigIndexObject(counter=i)
            f.set_random_properties()
            f.put()

            with finished_count_lock:
                global finished_count
                finished_count += 1

                if finished_count == num-1:
                    all_finished_event.set()

            # Get to Milestone A
            db.get(f.key())#,read_policy=db.EVENTUAL_CONSISTENCY)

        for i in range(num):
            thread.start_new_thread(run, (i,))

        all_finished_event.wait()

        # If the time to go from Milestone A -> B were longer, this would fail
        self.assertEquals(num, BigIndexObject.all().filter("counter >", -1).count())

